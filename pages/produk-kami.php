    <h2>Produk Kami</h2>

    <br>
    <b>1. Balap Motor / Road Race </b>
    <p>adalah cabang olahraga otomotif yang memperlombakan kemampuan mengendalikan motor di sirkuit.</p>

    <b>2. DRAG BIKE </b>
    <p>adalah kejuaraan mengendarai sepeda motor dengan kecepatan tinggi yang dilakukan didalam sebuah lintasan pacu aspal yang tertutup yang terdiri dari dua buah jalur lurus sejajar dengan panjang yang sama.</p>


    <b>3. Motocross </b>
    <p>adalah perlombaan Olahraga Otomotif roda 2 (dua) yang memiliki rintangan dan mempergunakan lintasan tanah.</p>

    <b>4. Grasstrack </b>
    <p>adalah sebuah ajang balapan yang menggunakan lintasan tanah serta handicap yang berbeda dengan lintasan Motocross, serta membatasi kapasitas mesin motornya yang di gunakan pada umumnya dari 110 cc sampai 125 cc, dengan beberapa kelas yaitu dari kelas bebek 4 tak open, kelas bebek 4 tak pemula, kelas bebek 2tak-4tak campuran dimana motor yang di gunakan bebas dan memenuhi spesifikasi tersebut.</p>

    <b>5. Supermoto</b>
    <p>adalah jenis Olahraga Otomotif mempergunakan kendaraan yang dirancang untuk kemampuan ekstrim, dimana sepeda motor ini adalah kendaraan balap yang bisa dipacu di 3 lintasan berbeda, yaitu lintasan road race, lintasan motocross dan lintasan datar, sehingga dinamakan Supermoto.</p>

    <b>6. Ikatan Motor Indonesia (IMI)</b>
    <p>berdedikasi untuk meningkatkan keselamatan pada setiap pengguna jalan di Indonesia. IMI sebagai pilar utama olahraga otomotif di Tanah Air turut berpartisipasi dalam menyelenggarakan program kampanye aman berkendara bertajuk #3500LIVES yang baru saja diluncurkan Fédération Internationale Internasioanal Automoile (FIA) di Paris, Perancis pada 10 Maret lalu.</p>
